import React, { createContext, useContext, useState, useEffect } from 'react';

interface User {
  id: string;
  email: string;
  full_name: string;
  trial_ends_at: string;
  role?: string;
}

interface AuthContextType {
  user: User | null;
  token: string | null;
  environment: string | null;
  group: string | null;
  company: string | null;
  companyId: string | null;
  cnpj: string | null;
  login: (data: any) => void;
  logout: () => void;
  switchCompany: (id: string, name: string, cnpj: string) => void;
  isAuthenticated: boolean;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const AuthProvider = ({ children }: { children: React.ReactNode }) => {
  const [user, setUser] = useState<User | null>(null);
  const [token, setToken] = useState<string | null>(null);
  const [environment, setEnvironment] = useState<string | null>(null);
  const [group, setGroup] = useState<string | null>(null);
  const [company, setCompany] = useState<string | null>(null);
  const [companyId, setCompanyId] = useState<string | null>(null);
  const [cnpj, setCnpj] = useState<string | null>(null);

  useEffect(() => {
    // Restore session from localStorage
    const storedToken = localStorage.getItem('token');
    const storedUser = localStorage.getItem('user');
    const storedEnv = localStorage.getItem('environment');
    const storedGroup = localStorage.getItem('group');
    const storedCompany = localStorage.getItem('company');
    const storedCompanyId = localStorage.getItem('companyId');
    const storedCnpj = localStorage.getItem('cnpj');

    if (storedToken && storedUser) {
      setToken(storedToken);
      setUser(JSON.parse(storedUser));
      setEnvironment(storedEnv);
      setGroup(storedGroup);
      setCompany(storedCompany);
      setCompanyId(storedCompanyId);
      setCnpj(storedCnpj);

      // Refresh user profile from server to ensure role and trial status are up to date
      fetch('/api/auth/me', {
        headers: { Authorization: `Bearer ${storedToken}` }
      })
      .then(res => {
        if (res.ok) return res.json();
        if (res.status === 401) {
          // Token expired or invalid
          localStorage.clear();
          window.location.href = '/login';
          throw new Error('Session expired');
        }
        throw new Error('Failed to refresh user data');
      })
      .then(userData => {
        setUser(userData);
        localStorage.setItem('user', JSON.stringify(userData));
      })
      .catch(err => console.error("Session refresh error:", err));
    }
  }, []);

  const login = (data: any) => {
    setToken(data.token);
    setUser(data.user);
    setEnvironment(data.environment_name);
    setGroup(data.group_name);
    setCompany(data.company_name);
    setCompanyId(data.company_id);
    setCnpj(data.cnpj);

    localStorage.setItem('token', data.token);
    localStorage.setItem('user', JSON.stringify(data.user));
    localStorage.setItem('environment', data.environment_name || '');
    localStorage.setItem('group', data.group_name || '');
    localStorage.setItem('company', data.company_name || '');
    localStorage.setItem('companyId', data.company_id || '');
    localStorage.setItem('cnpj', data.cnpj || '');
  };

  const logout = () => {
    setUser(null);
    setToken(null);
    setEnvironment(null);
    setGroup(null);
    setCompany(null);
    setCompanyId(null);
    setCnpj(null);
    localStorage.clear();
    window.location.href = '/login';
  };

  const switchCompany = (id: string, name: string, newCnpj: string) => {
    setCompany(name);
    setCompanyId(id);
    setCnpj(newCnpj);
    localStorage.setItem('company', name);
    localStorage.setItem('companyId', id);
    localStorage.setItem('cnpj', newCnpj);
    // Optional: Reload to ensure all components refresh data with new company context
    window.location.reload(); 
  };

  return (
    <AuthContext.Provider value={{ 
      user, 
      token, 
      environment, 
      group, 
      company,
      companyId,
      cnpj,
      login, 
      logout,
      switchCompany,
      isAuthenticated: !!user 
    }}>
      {children}
    </AuthContext.Provider>
  );
};

export const useAuth = () => {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
};
